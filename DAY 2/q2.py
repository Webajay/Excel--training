# Q.2 Write a program for assignment operators

a = int(input(" Enter Your Number: "))
b = int(input(" Enter Your Number: "))

# using the addition operator
a += b      # a = a + b

print(a)

# subtraction
a -= b      # a = a - b   

print(a)

# multiplication
a *= b      # a = a * b  

print(a)

# division
a /= b      # a = a / b

print(a)

# modulo
a %= 10 # a = a % 10 

print(a)

# a to the power b
a **= 10 # a = a ** 10

print(a)