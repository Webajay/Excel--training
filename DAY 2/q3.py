# Q.3Write a program for Bitwise operators

a = int(input(" Enter Your Number: "))
b = int(input(" Enter Your Number: "))

# using the Bitwise AND
print("a & b =", a & b)     # a = a & b

# using Bitwise OR
print("a | b =", a | b)     # a = a | b   

# using Bitwise NOT
print("~a =", ~a)   # a = ~a  

#using Bitwise  XOR
print("a ^ b =", a ^ b)     # a = a ^ b

# using Bitwise right shift
print("a >> 1 =", a >> 1)
print("b >> 1 =", b >> 1)

# using Bitwise left shift
print("a << 1 =", a << 1)
print("b << 1 =", b <<1)
